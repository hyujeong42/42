#include <unistd.h>

int	ft_strlen(char *argv)
{
	int	i;

	i = 0;
	while (argv[i] != '\0')
	{
		i++;
	}
	return (i);
}

int	main(int argc, char *argv[])
{
	int	i;

	i = argc;
	write(1, argv[0], ft_strlen(argv[0]));
	write(1, "\n", 1);
}
